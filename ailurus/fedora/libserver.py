def __set1():
    ret = []
    path = A+'/support/fedora_server_list'
    with open(path) as f:
        contents = [l.strip() for l in f]
    for i in range(0, len(contents), 3):
        country = contents[i]
        org = contents[i+1]
        url = contents[i+2]
        ret.append([country, org, url])
    return ret
    
